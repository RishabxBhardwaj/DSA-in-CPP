#include <bits/stdc++.h>
using namespace std;

int main()
{
  // code here

  // Implicit:
  cout << 10 / 3 << endl;
  cout << 10 / 3.0 << endl;
  cout << 'A' + 1 << endl;
  cout << "a" - 0 << endl;

  // Explicit:
  float pi = 3.14;
  cout << (int)('A') << endl;
  cout << (int)(pi) << endl;

  cout << ((float)10 / 3) << endl;

  return 0;
}

/*
  Typecasting:

    Implicit conversion: (Automatic/ type promotion). Done by compiler to avoid data loss

    Typepromotion

    BOOL -> CHAR -> INT -> FLOAT -> DOUBLE


    Explicit conversion: forced bt programmer
*/