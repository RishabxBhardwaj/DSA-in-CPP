#include <bits/stdc++.h>
using namespace std;

// ASCII : American Standard Code for Information Interchange (7bit character set (128 ch))

int main() {
  // code here
  char ch1 = 'a';
  char ch2 = 'X';
  char ch3 = '&';
  char ch4 = '9';
  char ch5 = '\n';

  cout << ch1 << endl;
  cout << ch2 << endl;
  cout << ch3 << endl;
  cout << ch4 << endl;
  cout << ch5 << endl;

  cout << (int) 'A' << endl;

  return 0;
}

/*
    Char -> 1 Byte

    ASCII Value: 'a' -> 97
    ASCII Value: 'A' -> 65

*/